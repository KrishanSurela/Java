package Bit_Manipulation;

public class A {
    public static void main(String[] args) {
        //Binary AND
        System.out.println(5&6); //4
        // Binary OR
        System.out.println(5|6); //7
        //Binary XOR
        System.out.println(5^6); // 3 (same same zero)
        // Binary 1's Compliment
        System.out.println(~5); //-6
        System.out.println(~-6); //5

        //Binary Left Shift
        System.out.println(5<<2); // 20 (a*2^b)
        // Binary Right Shift
        System.out.println(6>>1); // 3 (a/2^b)
}
    }
